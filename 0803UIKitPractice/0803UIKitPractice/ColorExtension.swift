import UIKit

extension UIColor {

    class var skyBlue: UIColor {
        return UIColor(red: 163.0 / 255.0, green: 214.0 / 255.0, blue: 253.0 / 255.0, alpha: 1.0)
    }

    class var lightGray: UIColor {
        return UIColor(red: 211.0 / 255.0, green: 221.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }

}
